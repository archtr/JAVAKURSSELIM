package Gun14;

public class _02_WhileLoop {
    public static void main(String[] args) {
        // ekrana 5 kez merhaba yazdırınız.

        int sayac=0;

        // sayaç 5 den küçük IKEN(WHILE)
        while (sayac<5){ // şart sağlanıyorsa parantezlerinin arasını çalıştır.
            System.out.println("Merhaba");

            sayac++;
        }



    }
}
