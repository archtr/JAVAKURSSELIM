package Gun15;

public class _02_ForLoop {
    public static void main(String[] args) {
        // 1 den 5 e kadar olan sayıları ekrana yazdırınız.

        int i=1; // sayaç
        while (i<=5){
            System.out.println("w-i= " +i);
            i++;
        } // i= 1,2,3,4,5

        // for döngüsü

        for (i=1;i<=5;i++){
            System.out.println("f-i= " +i);
        }



    }
}
